public class AvgThreeno {

   public static double avgthree(int a , int b, int c){
        
    int sum = a+b+c;

    double avg=sum/3;


    return avg;



   }

    public static void main(String[] args) {
        

        System.out.println(avgthree(5, 6, 7));
    }
    
}
